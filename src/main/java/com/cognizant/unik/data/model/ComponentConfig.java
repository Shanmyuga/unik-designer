package com.cognizant.unik.data.model;

public class ComponentConfig {

	private String componentPrefix;
	
	private String styleExt;

	/**
	 * @return the componentPrefix
	 */
	public String getComponentPrefix() {
		return componentPrefix;
	}

	/**
	 * @param componentPrefix the componentPrefix to set
	 */
	public void setComponentPrefix(String componentPrefix) {
		this.componentPrefix = componentPrefix;
	}

	/**
	 * @return the styleExt
	 */
	public String getStyleExt() {
		return styleExt;
	}

	/**
	 * @param styleExt the styleExt to set
	 */
	public void setStyleExt(String styleExt) {
		this.styleExt = styleExt;
	}
	
}
